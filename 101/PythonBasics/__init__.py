# 🐍 Introduction to Python

# 1️⃣ What is Python?
# - Python is a high-level, interpreted programming language.
# - It is beginner-friendly because of its simple and readable syntax.
# - Python is dynamically typed (no need to declare variable type).
# - It is open-source and has a huge community support.

# Example:
print("Hello, World!")   # ✅ First Python program


# 2️⃣ Why Python? (Where is it used?)
# - Web Development (Flask, Django, FastAPI).
# - Data Science & Machine Learning (NumPy, Pandas, TensorFlow, Scikit-learn).
# - Artificial Intelligence & Deep Learning (PyTorch, Keras).
# - Cloud Computing & DevOps (AWS SDK boto3, Ansible).

# ⚡ In short: Python is popular because it's easy to learn + powerful + versatile.


# 🐍 Python vs ☕ Java
# ────────────────────────────────────────────────────────────────
# Feature              | Python                              | Java
# ────────────────────────────────────────────────────────────────
# Type of Language     | Interpreted, dynamically typed      | Compiled, statically typed
# ────────────────────────────────────────────────────────────────
# Syntax               | Simple, concise, beginner-friendly | Verbose, strict (braces & semicolons)
# ────────────────────────────────────────────────────────────────
# Speed                | Slower (interpreted)               | Faster (JIT compiled to bytecode)
# ────────────────────────────────────────────────────────────────
# Memory Management    | Automatic (Garbage Collector)      | Automatic (Garbage Collector)
# ────────────────────────────────────────────────────────────────
# Use Cases            | Web, AI/ML, Data Science,          | Enterprise apps, Android apps,
#                      | Automation, Scripting, Prototyping | Banking/Finance systems
# ────────────────────────────────────────────────────────────────
# Community Support    | Huge (AI/Data/Web libraries)       | Huge (Enterprise/Android ecosystem)
# ────────────────────────────────────────────────────────────────
# Portability          | Portable but slower                | High (JVM → "Write once, run anywhere")
# ────────────────────────────────────────────────────────────────
# Learning Curve       | Easy (closer to English)           | Steeper (deep OOP concepts required)
# ────────────────────────────────────────────────────────────────
# Popular Libraries    | NumPy, Pandas, TensorFlow,         | Spring, Hibernate, JavaFX
#                      | Flask, Django                      |
# ────────────────────────────────────────────────────────────────
# Best For             | Beginners, AI/ML, rapid apps       | Enterprise, Android, large-scale systems
# ────────────────────────────────────────────────────────────────
